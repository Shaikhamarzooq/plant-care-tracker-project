def plant_care():
    today = datetime.now()
#    id_by_user= int(input('Enter a plant_id from the list of plant_id above :'))	

    plant_freq = {}
   
#read csv files and list it for user to chose a plant
    try:
        with open('plants_data.csv', 'r',newline = '\n') as file: 
            reader = csv.DictReader(file)
            print('Available Plants:')
            
            for i in reader:
                print(f"{i['plant_id']},'-', {i['plant_name']}")
          
        # if it was string          
        id_by_user= input('Enter a plant_id from the list of plant_id above :').strip()
        
        
        with open('plants_data.csv', 'r',newline = '\n') as file: 
            reader = csv.DictReader(file)
            
            for i in reader:
                if i ['plant_id'] == id_by_user:
                    plant_freq = i
                    break
             
            if not plant_freq:
                print("Choose from the list of plant_id above:")
                return
             
    except ValueError:
            print("Invalid number. Choose from the list of plant_id above: ")
          
            return

   
    # to store latest date
    latest_date = {'watered': None, 'fertilized':None, 'repotted': None, 'pruned': None}
    task= ['watered', 'fertilized', 'repotted', 'pruned']
    
    try:
        with open('activity_log.csv', 'r',newline = '\n') as file: 
            reader = csv.DictReader(file)
        
            for a in reader:
                if a ['plant_id'] == id_by_user:
                # to get date format
                    l_date = datetime.strptime(a['date'], '%d-%m-%Y')
                    
                   #update latest_date 
                    for t in task:
                       if a [t].lower() == 'true':
                           if latest_date[t] is None or l_date > latest_date[t]:
                               latest_date[t] = l_date
                        
                        
    except ValueError:
        print("Error. CSV not found ")
        return                
             

     # (frequency column , record column, task)
    care_task = [('watering_freq', 'watered', 'watering'), ('fertilizing_freq', 'fertilized', 'fertilizing'), ('repotting_freq', 'repotted', 'repotting'), ('pruning_freq', 'pruned', 'pruning')]
    
    for freq_l, log_k, label in care_task:
        last_date = latest_date [log_k]
        freq = int (plant_freq[freq_l])
        
        # for first care for new plant
        if last_date is None:
            print (f'{label}: No logs. Plant needs care')
        else:
            days_count = (today - last_date).days
            if days_count >= freq:
                print(f'{label}: need care')
            else: 
                print(f"{label}: dosen't need care")
 
    

