def add_plant():
    """
    This function allow the user to add a new plant by adding the data related to it:
    plant_name: string value that indicates the name of the plant
     plant_type: string but the user have to enter the integer represent that string: 
     {1: "summer", 2: "spring",3: "winter",4: "autumn",5: "all"}
    location :plant location but the user have to enter the integer represent it: 
    {1:"outdoor", 2: "hall",3: "kitchen",4:"living room",5:"bedroom"}
    date_acquired:date time value in format : day-month-year (dd-mm-yyyy)
    watering_freq:integer that represent number of days between two watering events
    sunlight_req:a string indicating the amount of sun light the plant need but the user have to enter the integer represent that string:
    {1:"low",2:"medium",3:"high"}
    fertilizing_freq:integer that represent number of days between two fertilizing events
    repotting_freq:integer that represent number of days between two repotting events
    pruning_freq:integer that represent number of days between two pruning events
    plant_id: this will be auto generated by the function at the end based on the maximum id in the plants_data.csv file
    
    """

    
    plant_name=input("enter the plant name please")
    
    plant_types = {1: "summer", 2: "spring",3: "winter",4: "autumn",5: "all"}


    while True:
        try:
            plant_type_input=int (input("enter the plant type number as a number between 1-5 (summer=1, spring=2,winter=3,autumn=4,all-seasons=5)"))
            if plant_type_input in plant_types:
                plant_type=plant_types[plant_type_input]
                break
            else:
                print("error: you must choose a number between 1-5 based on your plant type")
        except ValueError:
             
            print("value error: you must enter a number between 1 -5 for plant type")
        
    plant_locations = {1:"outdoor", 2: "hall",3: "kitchen",4:"living room",5:"bedroom"}

    while True:
        try:
            plant_location_input=int (input("enter the plant location number as a number between 1-5 (outdoor=1, hall=2, kitchen=3, living room=4, bedroom=5)"))
            if plant_location_input in plant_locations:
                plant_location=plant_locations[plant_location_input]
                break
            else:
                print("error: you must choose a number between 1-5 based on your plant location")
        except ValueError:
          
            print("value error:you must enter a number between 1 - 5 for plant location")

    while True:
        try:
            date_aquired_input=input("enter the date you aquire the plant in format: day(DD)-month(MM)-year(YYYY)")
            date_input=datetime.strptime(date_aquired_input, "%d-%m-%Y")
            if date_input.date()> datetime.today().date():
                print("you cannot enter a day in future")
            else:
                plant_aquired_date= date_input.strftime("%d-%m-%Y")
                break
        except ValueError:
            print("Invalid date format. Please use DD-MM-YYYY.")

    sun_req_frequencies={1:"low",2:"medium",3:"high"}
    while True:
        try:
            plant_sun_req_input=int (input("enter the plant sun light need as number between 1-3:(low=1,medium=2,high=3)"))
            if plant_sun_req_input in sun_req_frequencies:
                plant_sun_requirement=sun_req_frequencies[plant_sun_req_input]
                break
            else:
                print("error: you must choose a number between 1-3 based on your plant need to sun light")
        except ValueError:
             
            print("value error: you must enter a number between 1 -3 for plant need of sun")

    while True:
        try:
            watering_frequency_input=int(input("enter the plant watering frequency in days between (1-35 days)"))
            if watering_frequency_input<1 or watering_frequency_input>35:
                print("the plant watering days frequency must be between 1 day and 35 days")
            else:
                watering_frequency=watering_frequency_input
                break
        except ValueError:
            print("value error: you must enter a number between 1 -35 for plant need of water")

    while True:
        try:
            fertilizing_frequency_input=int(input("enter the plant fertilizing frequency in days between (14-180 days)"))
            if fertilizing_frequency_input<14 or fertilizing_frequency_input>180:
                print("the plant fertilizing days frequency must be between 14 day and 180 days")
            else:
                fertilizing_frequency=fertilizing_frequency_input
                break
        except ValueError:
            print("value error: you must enter a number between 14 -180 for plant need of fertilizing")

    while True:
        try:
            repotting_frequency_input=int(input("enter the plant repotting frequency in days between (90-1095 days)"))
            if repotting_frequency_input<90 or repotting_frequency_input>1095:
                print("the plant repotting days frequency must be between 90 day and 1095 days")
            else:
                repotting_frequency = repotting_frequency_input
                break
        except ValueError:
            print("value error: you must enter a number between 90 -1095 for plant need of repotting")


    while True:
        try:
            pruning_frequency_input=int(input("enter the plant pruning frequency in days between (7-365 days)"))
            if pruning_frequency_input<7 or pruning_frequency_input>365:
                print("the plant pruning days frequency must be between 7 day and 365 days")
            else:
                pruning_frequency = pruning_frequency_input
                break
        except ValueError:
            print("value error: you must enter a number between 7 -365 for plant need of pruning")
    try:        
        with open('plants_data.csv', 'r',newline='') as file:
            content=csv.DictReader(file)
            max_id = 0
            for i in content:
                current_id = int(i["plant_id"])
                if current_id > max_id:
                    max_id = current_id
        new_id=max_id+1
    except FileNotFoundError:
        max_id = 0
    new_plant = {
    "plant_id": new_id,
    "plant_name":plant_name,
    "plant_type":plant_type,
    "location":plant_location,
    "date_acquired":plant_aquired_date,
    "watering_freq":watering_frequency,
    "sunlight_req":plant_sun_requirement,
    "fertilizing_freq":fertilizing_frequency,
    "repotting_freq":repotting_frequency,
    "pruning_freq":pruning_frequency
    }
    # to add a new entry to my data
    with open('plants_data.csv', mode='a', newline='') as file:
        fieldnames = [
        "plant_id", "plant_name", "plant_type", "location",
        "date_acquired", "watering_freq", "sunlight_req",
        "fertilizing_freq", "repotting_freq", "pruning_freq"]
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        if file.tell() == 0:   
            writer.writeheader()
        writer.writerow(new_plant)
        print(f"{plant_name} added successfully with ID {new_id}")

    

def activity_capture():

    """
    This function will allow selecting the plant by plant ID and then select the desired activity
    """

#Below will read the plant_id and plant_name from the plant_data file

    plants = []
    with open('plants_data.csv','r',newline = '') as file:
        content = csv.DictReader(file)
        print(f'\nPlant List:\n')
        for i in content:
            plants.append(i)
            print(f'{i['plant_id']} | {i['plant_name'].upper()}')
          
#Below will read user's input, where he/she will select a plant and the activity to be performed
    
    plant_id = input('\n**Enter plant ID from the above list: ')
    
    plant_name = ''
    
    for i in plants:
        if plant_id == i['plant_id']:
            plant_name = i['plant_name'].upper()
            break

#Below will check if the plant ID is valid
    
    if plant_name:
        print(f'\n**The selected plant is {plant_id} : {plant_name}')
    else:
        print('The entered plant ID is incorrect!')
        return
        
    activity = input('\n1.Watering. \n2.Fertilizing. \n3.Repotting. \n4.Pruning \n\n**Enter the number/s from the above activity separated by comma : ').split(",")
    
    plant_activity = {'date': datetime.now().strftime('%d-%m-%Y'),
                      'plant_id': plant_id,
                      'watered': False,
                      'fertilized': False,
                      'repotted': False,
                      'pruned': False}

#Below will check if the activity is valid

    for i in activity:      
        if i == '1':
            plant_activity['watered']= True
        elif i == '2':
            plant_activity['fertilized']= True
        elif i == '3':
            plant_activity['repotted']= True
        elif i == '4':
            plant_activity['pruned']= True  
        else:
            return f'Activity enter is incorrect!'

#Below will append the activity into the activity_log file   
    
    activity_log_headers = ['date','plant_id','watered','fertilized','repotted','pruned']  
    with open('activity_log.csv', 'a', newline='\n') as activity_log:
        plantlog_writer = csv.DictWriter(activity_log, fieldnames = activity_log_headers)
        if activity_log.tell() == 0:   
           plantlog_writer.writeheader()
        plantlog_writer.writerow(plant_activity)

    print(f'\n Activity log been saved')
    print(f'\n {plant_activity}')

